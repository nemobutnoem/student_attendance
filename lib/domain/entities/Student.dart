class Student {

  final int id;
  final int studentId;
  final String name;
  final String studentCode;
  final String email;
  final String phone;
  final int universityId;

  Student({
    required this.id,
    required this.studentId,
    required this.name,
    required this.studentCode,
    required this.email,
    required this.phone,
    required this.universityId,
  });
}